<?php
function suma($n1,$n2){
	$resultado = $n1+$n2;
	return $resultado;
}
function resta($n1,$n2){
	$resultado = $n1-$n2;
	return $resultado;
}
function multiplicacion($n1,$n2){
	$resultado = $n1*$n2;
	return $resultado;
}
function division($n1,$n2){
	$resultado = $n1/$n2;
	return $resultado;
}

?>